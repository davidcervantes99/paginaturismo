                            <h1 style="color: #FC4C00" id="nuestrahistoria" class="display-5 fw-bolder " >NUESTRA HISTORIA</h1>
                            <p style="color: #DF9D49">Fue originalmente una ranchería habitada por indios pimas que dieron la obediencia a las autoridades españolas a mediados del siglo XVII: sus minas fueron descubiertas en 1760, y en 1762 los trabajos estaban en decadencia por la falta de seguridad y porque no había acertado el beneficio. El mineral  tuvo varios en el siglo XVIII y XIX. El desarrollo colonial de Cananea fue influido por las cercanas misiones de Arizpe, Santa María y Cocóspera. A mediados del siglo XVIII, Cananea pasa a ser parte de la provincia de Sonora, convirtiéndose en un real de minas. Poco antes de 1860 el general Ignacio Pesqueira adquirió las primeras propiedades mineras, mismas que en 1883 adquirió el anglo americano B .Benhan quien organizó la Cananea Mining Co. A fines del siglo XIX llegó a Cananea el empresario William Cornell Greene quien organizó en 1896 The Cananea Cooper Co.</p>
                            <p style="color: #DF9D49">En 1917 The Cananea Consolidated Cooper Co. es vendida a The Anaconda Mining Co. En 1940 se inicia la explotación de la mina a cielo abierto. En 1971 se mexicaniza la mina pasando a ser la Compañía Minera de Cananea.</p>  
                            <p style="color: #DF9D49">Durante el siglo XIX con la transición de la minería colonial a la industrial, la explotación cuprífera de Cananea cobró mayor importancia en la región y a fines del siglo contaba con varias minas en operación; sobresaliendo por su importancia la de Cobre Grande en posesión de la familia Pesqueira.</p>
                            <p style="color: #DF9D49">Adquirió mayor relevancia en el proceso histórico nacional cuando William C. Green en 1899 fundó The Cananea Consolidate Copper Company, por medio de la compra de empresas ya existentes y la adquisición de nuevas concesiones mineras. Un suceso histórico muy importante para la población cananense acaeció en junio de 1906 cuando tuvo lugar la huelga registrada en el mineral. Cananea se constituye como cabecera municipal el 8 de Noviembre de 1901. </p>
                            <p style="color: #DF9D49">En el aspecto administrativo, el territorio del municipio fue dependencia del municipio de Fronteras, hasta que el decreto del 31 de octubre de 1901 le otorgó la categoría municipal. Poco después se cambió el asiento del perfecto político del distrito de Arizpe. Su territorio fue mermado en junio de 1937 al constituirse a expensas el municipio de Naco. </p>
